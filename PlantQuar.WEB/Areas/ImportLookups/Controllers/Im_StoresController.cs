                }
            }
        }

        //DELETE
        [HttpPost]
        public JsonResult DeleteIm_Stores(int ID)
        {
            try
            {
                DeleteParameters obj = new DeleteParameters();
                obj.id = ID;
                obj.Userid = (short)Session["UserId"];
                obj._DateNow = DateTime.Now;
