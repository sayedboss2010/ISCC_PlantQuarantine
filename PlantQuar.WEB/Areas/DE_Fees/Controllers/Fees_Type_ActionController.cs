        [HttpPost]
        public JsonResult GetFees_Type_ActionOptions()
        {
            try
            {
                var res = APIHandeling.getData("Fees_Type_Action?IsActive=1");
                var lst = res.Content.ReadAsAsync<List<Fees_Type_ActionDTO>>().Result;
                var Fees_Type_Actions = lst.Select(c => new { DisplayText = c.Name_Ar, Value = c.ID, }).OrderBy(s => s.DisplayText);

                return Json(new { Result = "OK", Options = Fees_Type_Actions });
