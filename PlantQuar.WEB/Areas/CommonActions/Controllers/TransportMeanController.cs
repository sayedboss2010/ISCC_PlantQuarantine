        // GET: CommonActions/TransportMean
        #region TransportMean
        [HttpPost]
        public JsonResult TransportMean_List()
        {
            try
            {
                var res = APIHandeling.getData("TransportMean_API?List=1");
                var lst = res.Content.ReadAsAsync<List<CustomOption>>().Result;
                return Json(new { Result = "OK", Options = lst });
            }
            catch (Exception ex)
            {
                APIHandeling.Insert_Error(Request.Url.AbsoluteUri.ToString(), ex.Message, "TransportMean_List");
                return Json(new { Result = "ERROR", Message = Enums.GetEnumDescription<Enums.Error>(Enums.Error.ErrorHappened) });
            }
        }
