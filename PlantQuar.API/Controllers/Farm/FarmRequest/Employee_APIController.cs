        EmployeeBLL<EmployeeDTO> cBLL = new EmployeeBLL<EmployeeDTO>();
        public HttpResponseMessage GetEmployeeCount()
        {
            try
            {
                Dictionary<string, object> dic = cBLL.GetCount();
                return Request.CreateResponse(API_HelperFunctions.getStatusCode(int.Parse(dic["state_Code"].ToString())), dic["obj"]);
            }
            catch (Exception ex)
            {
                return Request.CreateErrorResponse(HttpStatusCode.InternalServerError, ex.Message);
            }
        }
        public HttpResponseMessage GetAllEmployee(int all)
        {
            try
            {

                Dictionary<string, object> dic = cBLL.GetAllEmp(API_HelperFunctions.Get_DeviceInfo());

