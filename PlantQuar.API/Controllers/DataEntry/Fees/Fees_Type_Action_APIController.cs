using System;
using System.Collections.Generic;
using System.Net;
using System.Net.Http;
using System.Web.Http;
using System.Web.Http.Cors;
using PlantQuar.DTO.HelperClasses;
using PlantQuar.BLL.BLL.DataEntry.Fees;
