        public HttpResponseMessage GetVillageByCenterId_Web(int all, int CenterId)
        {
            try
            {
                Dictionary<string, object> dic = cBLL.GetVillageByCenterId_Web(CenterId, API_HelperFunctions.Get_DeviceInfo());
